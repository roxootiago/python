def somar(num1,num2):
    soma = num1 + num2
    print(f"{num1} + {num2} = {soma}")

def subtrair(num1,num2):
    sub = num1 - num2
    print(f"{num1} - {num2} = {sub}")

def multiplicar(num1,num2):
    mult = num1*num2
    print(f"{num1} x {num2} = {mult}")

def dividir (num1,num2):
    div = num1/num2
    print(f"{num1} รท {num2} = {div}")



